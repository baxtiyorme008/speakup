export default function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).end();

  const { code } = req.body;
  if (!code) return res.status(400).json({ ok: false, message: 'Please enter your access code.' });

  const student = STUDENTS.find(
    s => s.code.toUpperCase() === code.trim().toUpperCase()
  );

  if (!student) {
    return res.status(401).json({
      ok: false,
      message: 'Code not found. Please check with your teacher.'
    });
  }

  return res.status(200).json({ ok: true, name: student.name });
}
